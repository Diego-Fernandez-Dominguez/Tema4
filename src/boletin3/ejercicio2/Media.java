package boletin3.ejercicio2;

public class Media {

	//

	static double media(int numero1, int numero2) {
		double media;

		media = (numero1 + numero2) / 2;

		return media;
	}

	//

	static double media(double numero1, double numero2) {
		double media;

		media = (numero1 + numero2) / 2;

		return media;
	}

}
